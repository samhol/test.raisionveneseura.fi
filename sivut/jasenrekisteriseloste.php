##Rekisterin nimi

Raision Veneseura ry:n jäsenrekisteri

###Rekisterin pitämisen peruste

RVS kerää tiedot jäsenistään jäsenien oman ilmoituksen perusteella.

##Rekisterin käyttötarkoitus

Rekisterillä pidetään yllä lakien määräämät tiedot RVS:n jäsenistä.
Rekisteriä hyödynnetään RVS:n postituksissa (jäsentiedotteet, oma lehti,
jäsenmaksut yms.) sekä tiedotuksissa liitoille.
Rekisteritietoja ei saa käyttää markkinointiin.

##Rekisteröinnin yhteydessä jäsenestä talletettavat tiedot

1. Etu- ja sukunimi 
2. Lähiosoite 
3. Postinumero ja -toimipaikka 
4. Jäsennumero sekä vastaava viitenumero
5. Puhelinnumero (vapaaehtoinen) 
6. Sähköpostiosoite&nbsp;(vapaaehtoinen)
7. Jäsenmaksun tilanne (viimeksi maksetun vuoden luku)
8. Jaos (moottorivene = 1, purjevene = 2, nuorisojäsen = 3)
9. Lisäksi voidaan tallentaa tiedot eri postitusryhmiin kuulumisesta, kuten hallituksenjäsen, muu vastaava ryhmä.

##Henkilötietojen suojauksen periaatteet

Rekisteri säilytetään lukitussa tilassa. Tietotekninen tallennus on suojatun
yhteyden takana.

###Tietojen luovuttaminen

Tämän rekisterin tietoja ei luovuteta ulkopuolisille. Raision Veneseura ry voi
kuitenkin antaa postituslistan (postitustarrat) jollekin postituksen
lähettäjälle ja tällöin lähettäjä saa laittaa mukaan tietoja itsestään tai
markkinoimastaan tuotteesta tai palvelusta. Postituksen tulee kuitenkin näissä
tapauksissa sisältää myös RVS:n postia. Lähettäjä ei saa käyttää saamiaan
tietoja jälkeenpäin omiin markkinointitarkoituksiinsa, eikä säilyttää
postituslistaa.
