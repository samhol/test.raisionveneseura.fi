Veneseurojen jäsenillä on mahdollisuus katsastuttaa veneensä vuosittain. Raision veneseuran
piirissä venekatsastus on seuran veneille vapaaehtoinen, muitten veneseurojen
käytännöt vaihtelevat. Säännöllinen venekatsastus varmistaa osaltaan sen, että
oma veneesi on tiettyjen kriteerien mukaisesti varusteltu, riittävän
hyväkuntoinen ja siinä on tarvittavat ja varsinkin viranomaisten edellyttämät
turvallisuutta parantavat varusteet.

Katsastuksen tarkoituksena on ylläpitää ja kehittää veneilyturvallisuutta ohjaamalla ja opastamalla
veneilijöitä veneidensä hoitoa, varustamista ja varusteiden käyttötaitoa
koskevissa asioissa sekä turvallisuutta edistävien asenteiden omaksumisessa.
Lähtökohtana on veneilijöiden oma turvallisuus. Tarkoituksena on myös, että
veneily ei aiheuta turvallisuusriskiä kaupalliselle merenkululle eikä muille
vesillä liikkuville.

Useat vakuutusyhtiöt suovat alennuksia venevakuutusmaksuihin silloin, kun vene on
katsastettu. Vain katsastettu vene on oikeutettu käyttämään seuran omaa
perälippua. Katsastus ei poista veneen päällikön vastuuta miltään
veneturvallisuuden osa-alueelta.

Veneenomistaja voi itse valita omaa venettään ja veneilyharrastustaan parhaiten vastaavan
katsastusluokan veneelleen. Luokkia on useita, yhdestä neljään, joista 1.
luokka on vaativin ja tarkoitettu pitkiä avomerimatkoja tekeville veneille.
Tavanomaisin katsastusluokka on 3. luokka, joka kattaa sisävesi- ja
saaristoliikenteen sekä Suomen rannikkoliikenteen mutta sallii kuitenkin myös
hyvissä olosuhteissa lähimpiin naapurimaihin suuntautuvat venematkat lyhimpiä
avomeriosuuksia käyttäen.

###Venekatsastuksien ajankohdasta

Raision
veneseuran venekatsastukset alkavat tuttuun tapaan huhtikuussa Raision
Haahdenniemen venesatamassa. Katsastustoiminnan aloittaa erityinen
runkokatsastuspäivä lauantaina 18.4.2015 klo 10 alkaen. Silloin on ensimmäinen
mahdollisuus katsastuttaa veneen runko kuivalla maalla, ja se tulee toteuttaa
vähintään viiden vuoden välein tai silloin, kun runkokatsastukseen on jokin
erityinen syy kuten veneen omistajan vaihdos, merkittävä korjaus- tai muutostyö
yms.

Vuosikatsastus tulee toteuttaa nimensä mukaisesti vuosittain, ja se tulee toteuttaa vedessä
käyttökuntoiselle ja kaikin puolin valmiiksi varustellulle veneelle.

Toukokuussa runko- ja vuosikatsastukset jatkuvat säännöllisesti viikon välein
tiistai-iltaisin klo 18 alkaen, ensimmäisen kerran 12.5. ja viimeisen kerran
23.6. eli aina juhannuksen kynnykselle saakka. Katsastuspäivät löytyvät aina
myös seuran kotisivuilta.

