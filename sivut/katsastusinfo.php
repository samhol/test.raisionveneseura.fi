###Kuka voi katsastaa

Runko- ja vuosikatsastuksen voi toteuttaa sama tai eri oman seuran valtuutettu
katsastaja tai kuka tahansa jonkun muun veneseuran valtuutettu venekatsastaja.
Venekatsastajat voivat siis katsastaa myös muiden seurojen veneitä.
Edellytyksenä on kuitenkin, että katsastettava vene kuuluu johonkin
tunnustettuun veneilyliiton piiriin kuuluvaan veneseuraan. Rajoitus johtuu
siitä, että alan liitto, Suomen purjehdus ja veneily ry. ylläpitää
venekatsastustoimintaa ja kouluttaa seurojen venekatsastajat.

###Näin varaat katsastusajan

Halutusta katsastusajankohdasta tehdään varaus Raision Haahdenniemen kioskissa
säilytettävään varauskansioon. Merkitse listaan veneen nimi ja sen sijainti
sekä oma nimesi ja mieluusti myös puhelinnumerosi. Jos veneelle on tarpeen
toteuttaa <u>runkokatsastus</u>, varataan yksi 20 minuutin vuoro maissa
toteutettavaa tarkistusta varten. Tavanomaista <u>vuosikatsastusta</u> varten
varataan samalla tapaa yksi 20 minuutin vuoro, ja se tehdään käyttövalmiille
veneelle vedessä.

Runkokatsastusta seuraa aina myöhemmin vedessä toteutettava tavanomaista vuosikatsastusta
laajempi <u>peruskatsastus</u>, Sitä varten tulee varata tavallista enemmän
aikaa, kaksi 20 minuutin vuoroa, sillä peruskatsastuksen yhteydessä veneelle
kirjoitetaan uusi katsastuspöytäkirja ja venetodistus.

###Valmistautuminen katsastukseen

Tarkista
etukäteen, että veneen laitteet ovat toimintakunnossa ja että katsastusluokkaa
vastaava venevarustus löytyy veneestä. Huolla ennalta sammuttimet,
pelastautumisvälineet, tarkista nestekaasujärjestelmän kunto ja varmista
hätämerkinantolaitteiden kelpoisuusajat. Sähkölaitteiden, erityisesti
merenkulkuvalojen toimivuus kannattaa varmistaa itse ennen katsastustapahtumaa,
vaikka edellisestä tarkistuksesta, johtimien korjauksesta tai vaikka jonkin
polttimon vaihdosta olisikin vain vähän aikaa.

Varmista, että veneen asiakirjat ja todisteet ovat veneessä. Ota esille venetodistus,
edellinen katsastuspöytäkirja ja rekisteritodistus. Kun uudessa veneessä tai
vaihtoveneessä ei ole edellistä katsastuspöytäkirjaa, selvitä ennalta
katsastuspöytäkirjaan tulevat veneen mitta- ja muut teknilliset sekä omistajan
tiedot. Varaa mukaasi tarpeellinen määrä käteistä rahaa katsastusmaksua varten.

###Katsastuspöytäkirjasta ja venetodistuksesta

Uutta katsastuspöytäkirjaa aletaan täyttää jo runkokatsastuksen yhteydessä, ja
veneenomistajan tulee muistaa ottaa se mukaan peruskatsastukseen.

Katsastuspöytäkirja koostuu kaksisivuisesta kaksiosaisesta lomakkeesta, jonka valkoiset osat jäävät
ensimmäisen katsastuskauden jäljiltä seuran haltuun. Samansisältöiset keltaiset
katsastuspöytäkirjan osat jäävät puolestaan veneeseen, jossa niitä on tarkoitus
säilyttää veneilykauden ajan. Tulevat vuotuiset katsastusmerkinnät tehdään vain
veneessä oleville keltaisille sivuille, ja lomakkeeseen voidaan tehdä
enimmillään viiden vuoden katsastusmerkinnät.

Silloin kun säännönmukaisen runko- ja peruskatsastusvuoden jälkeen veneelle tehdään
samassa omistuksessa neljä tavanomaista vuosikatsastusta, palvelee keltainen
katsastuspöytäkirja täydet viisi vuotta, minkä jälkeen tulee runko- ja peruskatsastus
uusia ja kirjoittaa uudet paperit.

Joskus saattaa runkokatsastus jäädä käytännön syistä toteutumatta ajallaan, ja
veneelle voidaan toteuttaa kauden alkaessa vain peruskatsastus vedessä. Tilanne
on tavanomainen uutta venettä käyttöön otettaessa tai kun venettä vaihdetaan
kesken kauden. Tällöinkin täytetään uusi katsastuspöytäkirja soveltuvin osin,
ja runkokatsastus tulee toteuttaa jälkikäteen tilaisuuden tullen, kun se on
mielekästä, saman kauden lopussa tai seuraavan alussa. Viiden vuoden kelpoisuussääntö
pätee silloin peruskatsastuksen toteutuksesta, ei viivästyneestä
runkokatsastuksesta laskien. Jos veneen käytössä on välivuosi, ja vene on
poissa käytöstä vaikkapa yhden kauden, ei vuosikatsastusta tietenkään vaadita,
mutta ’tyhjä välivuosi’ huomioidaan lomakkeessa hyppäämällä sille kaudelle
varatun merkintäkohdan yli. Toisin sanoen veneen käyttämätöntä välivuotta ei
hyvitetä viiden vuoden runko- ja peruskatsastuksen kelpoisuutta laskettaessa.
Vene vanhenee kalenterin mukaan, on se sitten vedessä tai maissa!

Runkokatsastuksen yhteydessä täytetään venetodistus eli ”venekortti”, virallinen asiakirja, johon
tehdään merkinnät vuosikatsastuksista enimmillään viiden vuoden ajalta.
Venetodistus uusitaan aina samalla kertaa kuin otetaan käyttöön uusi katsastuspöytäkirja.

###Katsastusmaksuista

Seuramme omaksuman tavan mukaan vuosikatsastuksesta tai kaksiosaisesta
runko/peruskatsastuksesta peritään seuran jäseneltä sama 20 euron maksu. Toisin
sanoen runkokatsastuksesta ei peritä erikseen mitään maksua. Myöskään seuramme
uusilta jäseniltä ei peritä ensimmäisen vuoden katsastusmaksua. Uusien jäsenten
onkin syytä muistuttaa katsastajaa tästä seikasta asioidessaan ensimmäistä
kertaa seuramme katsastajan kanssa!

On suotavaa, että seuran veneiden katsastukset voidaan hoitaa pääsääntöisesti
ennalta kaavailtuna aikoina Raisiossa, sillä katsastajat tekevät työtään täysin
vapaaehtoisesti ilman korvausta. Katsastajien kanssa voi erikseen sopia myös
poikkeavista katsastusajoista ja -paikoista. Tällöin tulee katsastajalle
mahdollisesti koituvat kustannukset korvata. Vuosikatsastuksia on tehty myös
seuran saaritukikohdissa Virutholmissa ja Hirsiluodossa, ja niitä on
erityisesti <u>Mikko Vuolahti</u> toteuttanut erikseen sovittuina aikoina.

Katsastajat voivat tehdä katsastuksia erikseen sopien myös seuran ulkopuolisille veneille
sillä edellytyksellä, että vene kuuluu johonkin seuraan. Perittävä
katsastusmaksu on tällöin suurempi kuin seuran omilta jäseniltä peritty 20 eur.

Korostettakoon, että samoin kuin muukin seuran toiminta, katsastajien työ on palkatonta
vapaaehtoista toimintaa, ja perityillä katsastusmaksuilla veneseuramme
kustantaa katsastajien koulutuksen, katsastusmateriaalit yms.
katsastustoiminnan menot.

Kaikki seuran katsastajat opastavat mielellään veneilyasioissa ja neuvovat parhaansa
mukaan ’asiakkaitaan’. Kysykää veneilyasioista - ei kannata empiä jos mieltä on
askarruttanut jokin veneilyyn liittyvä seikka.
